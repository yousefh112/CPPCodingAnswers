#include <iostream>
using namespace std;
int main()
{
    long long x , y ;
    // input your two numbers
    cin >> x >> y;
    // printing the summation 
    cout << x << " + " << y << " = " << x + y << endl;
    // printing the multiplication 
    cout << x << " * " << y << " = " << x * y << endl;
    // printing the subtraction 
    cout << x << " - " << y << " = " << x - y << endl;

    return 0;
}